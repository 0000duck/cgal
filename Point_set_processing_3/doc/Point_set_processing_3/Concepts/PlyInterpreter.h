 /*!
\ingroup PkgPointSetProcessingConcepts
\cgalConcept

A PLY interpreter describes how to process a PLY input based on its
headers informations. More specifically, it must specify what
properties are expected for each point and where to store them once
read.

\cgalHasModel `CGAL::Ply_abstract_interpreter`
\cgalHasModel `CGAL::Ply_interpreter_points_and_normals_3`

*/
class PlyInterpreter
{
public:
  /*!

    Initialize the interpreter with a set of reader and check if
    expected properties are found in the PLY hreader.

    \param readers set of readers generated by the PLY header.

    \return true if the provided readers match the requirements of the
    interpreter.
   */
  bool init (const std::vector<Ply_read_number*>& readers);

  /*!
    Read input stream.
    \param stream Input stream.
   */
  void get_line (std::istream& stream);

  /*!
    Assign read properties to appropriate containers or classes.
  */
  void process_line ();

};
